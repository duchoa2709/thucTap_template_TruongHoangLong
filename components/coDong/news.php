<script>
// Hàm để cập nhật thời gian
function updateTime() {
    // Tạo một đối tượng Date mới để lấy thời gian hiện tại
    var now = new Date();

    // Lấy giờ, phút, giây, ngày, tháng, năm từ đối tượng Date
    var hours = now.getHours();
    var minutes = now.getMinutes();
    var seconds = now.getSeconds();
    var day = now.getDate();
    var month = now.getMonth() + 1; // JavaScript đếm tháng từ 0-11
    var year = now.getFullYear();

    // Thêm số 0 phía trước nếu giờ, phút, giây, ngày, tháng nhỏ hơn 10
    hours = (hours < 10) ? "0" + hours : hours;
    minutes = (minutes < 10) ? "0" + minutes : minutes;
    seconds = (seconds < 10) ? "0" + seconds : seconds;
    day = (day < 10) ? "0" + day : day;
    month = (month < 10) ? "0" + month : month;

    // Tạo chuỗi thời gian
    var timeString = hours + ":" + minutes + ":" + seconds + " " + day + "-" + month + "-" + year;

    // Lấy tất cả các phần tử có class 'current-time'
    var elements = document.getElementsByClassName("current-time");

    // Cập nhật nội dung của tất cả các phần tử có class 'current-time' với chuỗi thời gian
    for (var i = 0; i < elements.length; i++) {
        elements[i].innerHTML = timeString;
    }
}

// Cập nhật thời gian ngay lập tức
updateTime();

// Cập nhật thời gian mỗi giây
setInterval(updateTime, 1000);
</script>